package com.devculi.java8.stream.sort;

public class People {
	private int id;
	private int age;

	public People(int id, int age) {
		super();
		this.id = id;
		this.age = age;
	}

	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

	public int getAge() {
		return age;
	}

	public void setAge(int age) {
		this.age = age;
	}

}
